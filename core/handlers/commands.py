async def handle_time_input(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –≤–≤–æ–¥–∞ –≤—Ä–µ–º–µ–Ω–∏ –ø–µ—Ä–≤–æ–≥–æ –ø—Ä–∏—ë–º–∞ —Ç–∞–±–ª–µ—Ç–∫–∏.
    """
    user = update.effective_user
    text = update.message.text.strip()
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –æ–∂–∏–¥–∞–µ–º –ª–∏ –º—ã –≤–≤–æ–¥ –≤—Ä–µ–º–µ–Ω–∏ –æ—Ç —ç—Ç–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    if not context.user_data.get('awaiting_first_dose_time'):
        return  # –≠—Ç–æ –Ω–µ –≤–≤–æ–¥ –≤—Ä–µ–º–µ–Ω–∏, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º
    
    # –ü–∞—Ç—Ç–µ—Ä–Ω –¥–ª—è –≤—Ä–µ–º–µ–Ω–∏ –≤ —Ñ–æ—Ä–º–∞—Ç–µ –ß–ß:–ú–ú
    time_pattern = r'^([01]?[0-9]|2[0-3]):([0-5][0-9])$'
    match = re.match(time_pattern, text)
    
    if not match:
        vimes_error = """
üè¥‚Äç‚ò†Ô∏è **–ì—Ä–∞–∂–¥–∞–Ω–∏–Ω, —Ç—ã –∏–∑–¥–µ–≤–∞–µ—à—å—Å—è?**

–Ø —è—Å–Ω–æ —Å–∫–∞–∑–∞–ª: —Ñ–æ—Ä–º–∞—Ç **–ß–ß:–ú–ú**

–ü—Ä–∏–º–µ—Ä—ã –ø—Ä–∞–≤–∏–ª—å–Ω–æ–≥–æ –≤–≤–æ–¥–∞:
‚Ä¢ 08:30
‚Ä¢ 14:15  
‚Ä¢ 09:00
‚Ä¢ 21:45

–ü–æ–ø—Ä–æ–±—É–π –µ—â–µ —Ä–∞–∑. –ò –Ω–∞ —ç—Ç–æ—Ç —Ä–∞–∑ –≤–Ω–∏–º–∞—Ç–µ–ª—å–Ω–µ–µ.

*"–î–µ—Ç–∞–ª–∏ ‚Äî —ç—Ç–æ –Ω–µ –¥–µ—Ç–∞–ª–∏. –î–µ—Ç–∞–ª–∏ —Å–æ–∑–¥–∞—é—Ç –¥–∏–∑–∞–π–Ω."*
"""
        await update.message.reply_text(vimes_error, parse_mode='Markdown')
        return
    
    # –í—Ä–µ–º—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ, –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º
    hours, minutes = int(match.group(1)), int(match.group(2))
    first_dose_time = f"{hours:02d}:{minutes:02d}"
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—Ä–µ–º—è –≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏—Ö –¥–∞–Ω–Ω—ã—Ö
    context.user_data['first_dose_time'] = first_dose_time
    context.user_data['awaiting_first_dose_time'] = False
    context.user_data['program_started'] = True
    
    # –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º —Å–ª–µ–¥—É—é—â–∏–π –ø—Ä–∏—ë–º (—á–µ—Ä–µ–∑ 2 —á–∞—Å–∞)
    now = datetime.now()
    first_time = datetime.combine(now.date(), datetime.strptime(first_dose_time, "%H:%M").time())
    if first_time < now:
        first_time += timedelta(days=1)  # –ï—Å–ª–∏ –≤—Ä–µ–º—è —É–∂–µ –ø—Ä–æ—à–ª–æ, —Ç–æ –Ω–∞ —Å–ª–µ–¥—É—é—â–∏–π –¥–µ–Ω—å
    
    next_dose = first_time + timedelta(hours=2)
    
    vimes_confirmation = f"""
üè¥‚Äç‚ò†Ô∏è **–û—Ç–ª–∏—á–Ω–æ, –≥—Ä–∞–∂–¥–∞–Ω–∏–Ω {user.first_name}!**

–ó–∞–ø–∏—Å–∞–Ω–æ: –ø–µ—Ä–≤—ã–π –ø—Ä–∏—ë–º –≤ **{first_dose_time}**.

**–ü—Ä–æ–≥—Ä–∞–º–º–∞ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–∞.** ‚úÖ

–¢–µ–ø–µ—Ä—å –ì–æ—Ä–æ–¥—Å–∫–∞—è –°—Ç—Ä–∞–∂–∞ –±—É–¥–µ—Ç –Ω–∞–≤–µ–¥—ã–≤–∞—Ç—å—Å—è –∫ —Ç–µ–±–µ –∫–∞–∂–¥—ã–µ **2 —á–∞—Å–∞** –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–æ–±–ª—é–¥–µ–Ω–∏—è —Ä–µ–∂–∏–º–∞.

‚è∞ **–°–ª–µ–¥—É—é—â–µ–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ:** {next_dose.strftime("%H:%M")}

–ù–µ –≤–∑–¥—É–º–∞–π –ø—Ä–æ–ø—É—Å–∫–∞—Ç—å! –£ –∫–∞–ø–∏—Ç–∞–Ω–∞ –í–∞–π–º—Å–∞ —Ö–æ—Ä–æ—à–∞—è –ø–∞–º—è—Ç—å, –∏ –æ–Ω –ø–æ–º–Ω–∏—Ç –≤—Å–µ—Ö –Ω–∞—Ä—É—à–∏—Ç–µ–ª–µ–π —Ä–µ–∂–∏–º–∞.

*"–†–µ–≥—É–ª—è—Ä–Ω–æ—Å—Ç—å ‚Äî —ç—Ç–æ –≤–µ–∂–ª–∏–≤–æ—Å—Ç—å –∫–æ—Ä–æ–ª–µ–π. –ò –∫–∞–ø–∏—Ç–∞–Ω–æ–≤ —Å—Ç—Ä–∞–∂–∏ —Ç–æ–∂–µ."*

‚Äî –ö–∞–ø–∏—Ç–∞–Ω –í–∞–π–º—Å (—Ç–µ–ø–µ—Ä—å –æ—Ñ–∏—Ü–∏–∞–ª—å–Ω–æ —Å–ª–µ–¥–∏—Ç –∑–∞ —Ç–≤–æ–∏–º –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ–º)

**–ò—Å–ø–æ–ª—å–∑—É–π /stats –¥–ª—è –∫–æ–Ω—Ç—Ä–æ–ª—è –ø—Ä–æ–≥—Ä–µ—Å—Å–∞.**
"""
    
    try:
        await update.message.reply_text(
            vimes_confirmation, 
            parse_mode='Markdown'
        )
        logger.info(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user.id} —É—Å—Ç–∞–Ω–æ–≤–∏–ª –≤—Ä–µ–º—è –ø–µ—Ä–≤–æ–≥–æ –ø—Ä–∏—ë–º–∞: {first_dose_time}")
        
        # TODO: –í —Å–ª–µ–¥—É—é—â–∏—Ö —ç—Ç–∞–ø–∞—Ö –∑–¥–µ—Å—å –±—É–¥–µ—Ç –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π
        
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–∏ –≤—Ä–µ–º–µ–Ω–∏: {e}")
        await update.message.reply_text(
            "–ö–∞–ø–∏—Ç–∞–Ω –í–∞–π–º—Å –ø—Ä–∏–Ω—è–ª –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é, –Ω–æ —á—Ç–æ-—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫ —Å –∑–∞–ø–∏—Å—å—é. "
            "–ü–æ–ø—Ä–æ–±—É–π /start –µ—â–µ —Ä–∞–∑."
        )


