            "–ï–≥–æ –±–ª–æ–∫–Ω–æ—Ç –≥–¥–µ-—Ç–æ –ø–æ—Ç–µ—Ä—è–ª—Å—è –≤ –∞—Ä—Ö–∏–≤–∞—Ö –°—Ç—Ä–∞–∂–∏."
